/* 
Write a Java program to declare an integer variables, one float variable, and one string variable and assign 10, 12.5, and "Java programming" to them respectively. Then display their values on the screen.
*/

public class Exercise1 {
    public static void main( String[] args) {

        int integerVariable = 10;
        float floatVariable = 12.5f;
        String stringVariable = "Java programming";

        System.out.println("integer variable: " + integerVariable);
        System.out.println("float variable: " + floatVariable);
        System.out.println("String: " + stringVariable);

    }
}